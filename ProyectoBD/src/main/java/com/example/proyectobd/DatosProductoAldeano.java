package com.example.proyectobd;

public class DatosProductoAldeano {
    String nombreAldeano,categoria;
    int cantidad, precio;

    String nombreAldenoR,nombreRegion;
    int salario;

    public String getNombreAldenoR() {
        return nombreAldenoR;
    }

    public void setNombreAldenoR(String nombreAldenoR) {
        this.nombreAldenoR = nombreAldenoR;
    }

    public String getNombreRegion() {
        return nombreRegion;
    }

    public void setNombreRegion(String nombreRegion) {
        this.nombreRegion = nombreRegion;
    }

    public int getSalario() {
        return salario;
    }

    public void setSalario(int salario) {
        this.salario = salario;
    }

    public String getNombreAldeano() {
        return nombreAldeano;
    }

    public void setNombreAldeano(String nombreAldeano) {
        this.nombreAldeano = nombreAldeano;
    }

    public String getCategoria() {
        return categoria;
    }

    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }

    public int getCantidad() {
        return cantidad;
    }

    public void setCantidad(int cantidad) {
        this.cantidad = cantidad;
    }

    public int getPrecio() {
        return precio;
    }

    public void setPrecio(int precio) {
        this.precio = precio;
    }
}

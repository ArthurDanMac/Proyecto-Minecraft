package com.example.proyectobd;

public class DatosAldeano {
   static String nombre;
   static String regionAldeano;
   static int salario;
   static int idAldeano;

    public DatosAldeano() {
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getSalario() {
        return salario;
    }

    public void setSalario(int salario) {
        this.salario = salario;
    }

    public int getIdAldeano() {
        return idAldeano;
    }

    public void setIdAldeano(int idAldeano) {
        this.idAldeano = idAldeano;
    }

    public String getRegionAldeano() {
        return regionAldeano;
    }

    public void setRegionAldeano(String regionAldeano) {
        this.regionAldeano = regionAldeano;
    }
}
